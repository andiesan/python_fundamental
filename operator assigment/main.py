#operator assigment -> operasi dalam variable dalam penyimpanan

#example tambah (+)
a = 4 # adalah assigment
a += 2 # adalah a = 4 + 2 
print(a)

#example kurang (-)
a = 4 # adalah assigment
a -= 2 # adalah a = 4 - 2 
print(a)

#example kali (*)
a = 4 # adalah assigment
a *= 2 # adalah a = 4 * 2 
print(a)

#example bagi (/)
a = 4 # adalah assigment
a /= 2 # adalah a = 4 / 2 
print(a)

#example modulus (%)
a = 4 # adalah assigment
a %= 2 # adalah a = 4 + 2 
print(a)