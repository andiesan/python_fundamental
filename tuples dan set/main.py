#tuples dan set

# data list menggunakan kurung siku []
print("===data list===")
data_list = [ 0, 1, 12, 3, 4, 5 ]
print(f"data list {data_list}")

#data tuples -> menggunakan kurung biasa ()
print("===data tuples===")
data_tuples = ( 1, 2, 3, 4, 5 )

print(f"data list {data_list}")
print(f"data tuples {data_tuples}")

#mengambil isi data tuples
print(f"data tuples {data_tuples[1]}")
#data tuples :
# - tidak bisa dirubah
# - tidak dapat di assigment
# - data tetap
# - data yang tidak bisa di operasikan oleh property


#set -> data set menggunakan kurung kurawal {}
print("===data set===")
data_sets = { 1, 2, 3, 4, 5, 6 } 
#data set adalah himpunan yang hanya bisa di print
#data set juga tidak dapat diolah dengan property
print(f"data set {data_sets}")